

package com.mycompany.lathitung_fungsi_param;


public class Lathitung_fungsi_param {
    
    //membuat fungsi
    static int tambah(int a, int b)
    {
        return a+b;
    }
    
    public static void main(String[] args) {
        int a, b;
        a=100;
        b=200;
        System.out.println(tambah(a, b));
    }
}
