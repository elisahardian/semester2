
package com.mycompany.nestedloop;


public class Nestedloop {

    public static void main(String[] args) {
        for (int i =0 ; i < 5 ; i++)
        {
            for (int j = 0 ; j < 4 ; j++)
            {
                System.out.print("loop j ke: "+ j +"  ");
            }
            System.out.println("loop i ke: "+ i +"    ");
        }
    }
}


