
package com.mycompany.perulangan_for;

public class Perulangan_for {

    public static void main(String[] args) {
        int i;
        for (i=0; i<5; i++) 
        {
            System.out.println("pengulangan ke: "+ i);
        }
    }
}

//OUTPUT : akan mengulang sebanyak 5 kali, dari angka 0 sebagai nilai awal, sampai angka 4 terakhir, karena yang <5
// dengan pencacahan +1, setiap perulangan nilai ditambah 1, i=i+1